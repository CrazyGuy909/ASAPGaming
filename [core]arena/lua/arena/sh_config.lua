asapArena.KillStreakFunctions = {
    {
        Name = "Airstrike",
        Kills = 5,
        CanUse = function(ply)
            return not ply:HasWeapon("weapon_rpw_binoculars")
        end,
        OnUse = function(ply)
            ply:Give("weapon_rpw_binoculars")
            timer.Simple(.1, function()
                ply:SelectWeapon("weapon_rpw_binoculars")
            end)
        end,
    }, {
        Name = "Drone",
        Kills = 10,
        CanUse = function(ply)
            return not ply._droneRequested
        end,
        OnUse = function(ply)
            ply._doneRequested = true
            net.Start("ASAP.Arena:RequestDrone")
            net.WriteBool(true)
            net.Send(ply)
            local drone = ents.Create("sent_arena_drone")
            drone:SetPos(ply:GetPos())
            drone:Spawn()
            drone:SetDriver(ply)
        end,
        OnSpawn = function(ply)
            ply._doneRequested = false
        end
    }
}
